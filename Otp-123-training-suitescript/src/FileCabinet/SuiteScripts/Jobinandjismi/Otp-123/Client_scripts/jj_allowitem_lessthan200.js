    function validateLine(context) {
        var currentRecord = context.currentRecord;
        var amount = parseFloat(currentRecord.getCurrentSublistValue({
            sublistId: 'item',
            fieldId: 'amount'
        }));

        if (amount > 200) {
            return true; // Allow the item to be added
        } else {
            dialog.alert({
                title: 'Error',
                message: 'The amount must be greater than 200 to add this item.'
            });
            return false; // Prevent the item from being added
        }
    }

